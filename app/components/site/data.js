function buildStoreMapQuery(name, address) {
  return `Kenny Hills Bakers ${name}, ${address}`;
}

function buildMapEmbedUrl(name, address) {
  return `https://maps.google.com/maps?hl=en&q=${encodeURIComponent(buildStoreMapQuery(name, address))}&t=&z=15&ie=UTF8&iwloc=B&output=embed`;
}

function buildDirectionsUrl(name, address) {
  return `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(buildStoreMapQuery(name, address))}`;
}

function createStore({ id, name, address, phone, hours, state }) {
  return {
    id,
    name,
    address,
    phone,
    hours,
    state,
    mapUrl: buildMapEmbedUrl(name, address),
    googleMapsUrl: buildDirectionsUrl(name, address)
  };
}

